#!/bin/bash

if [ -n "$2" ]; then
	\cd $1 $2;
	echo -n "\033]0;${PWD/$HOME/~}\007";
elif [ -z "$1" ];then
	\cd;
	echo -n "\033]0;~\007";
elif [ ! -d "$1" -a "$1" != "-" ]; then
	echo "I'm pretty sure you're stoned again...";
else	
	\cd $1;
	echo -n "\033]0;${PWD/$HOME/~}\007";
fi;
